    <h4> About</h4>
    The Honda CR-X del Sol is a two-seater targa-top 
    car manufactured by Honda from 1992 until 1998. 
    Despite the body resemblance to a mid-engine car 
    design, the del Sol is based on the front-engined 
    Honda Civic platform and was the successor to the Honda CR-X.
    The Spanish name del Sol translates to of the sun, and 
    refers to the car's opening roof. The del Sol featured 
    a removable aluminum hardtop that stowed onto a hinged 
    frame in the trunk and a motorized drop-down rear window. 
    Manual and automatic "TransTop" roofs were available 
    in select markets. It is the first open-air Honda sold in 
    the United States.

Production and sales ended with the 1997 model in North America and 1998 elsewhere.
